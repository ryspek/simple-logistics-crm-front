<template>
  <VaForm ref="form" @submit.prevent="submit">
    <p class="text-base mb-4 leading-5">
    </p>
    <VaInput v-model="formData.username" :rules="[validators.required]" class="mb-4" label="Логин" type="text" />
    <VaValue v-slot="isPasswordVisible" :default-value="false">
      <VaInput
        v-model="formData.password"
        :rules="[validators.required]"
        :type="isPasswordVisible.value ? 'text' : 'password'"
        class="mb-4"
        label="Пароль"
        @clickAppendInner.stop="isPasswordVisible.value = !isPasswordVisible.value"
      >
        <template #appendInner>
          <VaIcon
            :name="isPasswordVisible.value ? 'mso-visibility_off' : 'mso-visibility'"
            class="cursor-pointer"
            color="secondary"
          />
        </template>
      </VaInput>
    </VaValue>

    <div class="flex justify-center mt-4">
      <VaButton class="w-full" @click="submit"> Вход</VaButton>
    </div>
  </VaForm>
</template>

<script lang="ts" setup>
import { reactive } from 'vue'
import { useRouter } from 'vue-router'
import { useForm, useToast } from 'vuestic-ui'
import { validators } from '../../services/utils'
import axiosInstance from '../../services/config/axiosConfig'

const { validate } = useForm('form')
const { push } = useRouter()
const { init } = useToast()

const formData = reactive({
  email: '',
  username: '',
  password: '',
  keepLoggedIn: false,
})

const submit = async () => {
  if (validate()) {
    try {
      const response = await axiosInstance.post('http://localhost:8080/api/auth/login', {
        username: formData.username,
        password: formData.password,
      })
      console.log(response)
      if (response.status === 200 && response.data.token) {
        // Store user data in localStorage
        localStorage.setItem('user', JSON.stringify(response.data))

        // Show success toast and navigate to dashboard
        init({ message: "Вы успешно вошли в систему", color: 'success' })
        push({ name: 'debtors' })
      } else {
        // Handle unsuccessful login
        init({ message: 'Вход в систему не удался', color: 'danger' })
      }
    } catch (error) {
      // Handle error from the backend
      init({ message: 'Возникла не предвиденная ошибка: ' + error.message, color: 'danger' })
    }
  }
}
</script>
